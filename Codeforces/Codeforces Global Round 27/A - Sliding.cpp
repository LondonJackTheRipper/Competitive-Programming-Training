void solve()
{
    int n, m, r, c; cin >> n >> m >> r >> c;

    ll res = 1ll * n * m - 1ll * (r - 1) * m - c - (n - r);
    int rem = n - r;
    res += 1ll * rem * m;

    cout << res << '\n';
}
